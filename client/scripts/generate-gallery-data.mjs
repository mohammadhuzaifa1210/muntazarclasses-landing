import { mkdir, readdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const clientRoot = path.resolve(__dirname, '..')
const galleryRoot = path.join(clientRoot, 'public', 'gallery')
const outputFile = path.join(clientRoot, 'src', 'data', 'galleryData.generated.js')

// Felicitation is first; remaining albums run from the newest academic year down.
const albums = [
  { title: 'Felicitation', folder: 'felicitation', icon: 'award' },
  { title: 'Annual Function 2026', folder: 'annual-function-2026', icon: 'star' },
  { title: 'Annual Sports Girls 2026', folder: 'annual-sports-girls-2026', icon: 'activity' },
  { title: 'Annual Sports Boys 2026', folder: 'annual-sports-boys-2026', icon: 'activity' },
  { title: "Teacher's Day 2026", folder: 'teachers-day-2026', icon: 'heart' },
  { title: 'Only Girls picnic to Monteria Village 2026', folder: 'girls-picnic-monteria-2026', icon: 'sun' },
  { title: 'Only boys trip to Murud, Alibaug 2026', folder: 'boys-trip-murud-alibaug-2026', icon: 'map' },
  { title: 'Only Girls trip to Matheran, 2026', folder: 'girls-trip-matheran-2026', icon: 'map' },
  { title: 'Students involvement in Class', folder: 'students-involvement-in-class', icon: 'book' },
  { title: 'Movie time with students in class', folder: 'movie-time-with-students', icon: 'camera' },
  { title: 'Prelim exam for boards 2025', folder: 'prelim-exam-boards-2025', icon: 'book' },
  { title: 'S.S.C. Batch 2024-25', folder: 'ssc-batch-2024-25', icon: 'graduation' },
  { title: 'Annual Function 2024-25', folder: 'annual-function-2024-25', icon: 'star' },
  { title: 'Weekly tests', folder: 'weekly-tests', icon: 'book' },
  { title: 'Visit to Nehru Science Centre 2024', folder: 'nehru-science-centre-2024', icon: 'science' },
  { title: 'Annual Function 2015-16', folder: 'annual-function-2015-16', icon: 'star' },
]

const imageExtensions = new Set(['.avif', '.gif', '.jpeg', '.jpg', '.png', '.webp'])

await mkdir(galleryRoot, { recursive: true })
const galleryData = await Promise.all(albums.map(async (album) => {
  const albumPath = path.join(galleryRoot, album.folder)
  await mkdir(albumPath, { recursive: true })
  const entries = await readdir(albumPath, { withFileTypes: true })
  const images = entries
    .filter((entry) => entry.isFile() && imageExtensions.has(path.extname(entry.name).toLowerCase()))
    .map((entry) => `/gallery/${album.folder}/${encodeURIComponent(entry.name)}`)
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))
  return { ...album, images }
}))

// Include any additional album folders added later, so photos are never skipped.
const knownFolders = new Set(albums.map((album) => album.folder))
const extraFolderEntries = await readdir(galleryRoot, { withFileTypes: true })
const extraAlbums = await Promise.all(extraFolderEntries
  .filter((entry) => entry.isDirectory() && !knownFolders.has(entry.name))
  .map(async (entry) => {
    const albumPath = path.join(galleryRoot, entry.name)
    const images = (await readdir(albumPath, { withFileTypes: true }))
      .filter((file) => file.isFile() && imageExtensions.has(path.extname(file.name).toLowerCase()))
      .map((file) => `/gallery/${encodeURIComponent(entry.name)}/${encodeURIComponent(file.name)}`)
      .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))
    return { title: entry.name, folder: entry.name, icon: 'camera', images }
  }))

galleryData.push(...extraAlbums)

await mkdir(path.dirname(outputFile), { recursive: true })
await writeFile(outputFile, `// This file is generated by scripts/generate-gallery-data.mjs. Do not edit it manually.\nexport default ${JSON.stringify(galleryData, null, 2)}\n`)
